    private void btnKayitOlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKayitOlActionPerformed
        // TODO add your handling code here:
        if(txtKayitYas.getText().isEmpty() && txtKayitTCKN.getText().isEmpty()){
            txtKayitKullanici.setText("");
        }
        else{
            String firstPart = "";
            if (isUserKayitOk) firstPart = "101";
            if(isPersonelKayitOk) firstPart = "202";
            if(isYoneticiKayitOk) firstPart = "302";
            firstPart+=txtKayitYas.getText();
            firstPart+=txtKayitTCKN.getText().charAt(0);
            firstPart+=txtKayitTCKN.getText().charAt(1);
            firstPart+=txtKayitTCKN.getText().charAt(9);
            firstPart+=txtKayitTCKN.getText().charAt(10);
            txtKayitKullanici.setText(firstPart);
        }
        if(txtKayitAd.getText().isEmpty() || txtKayitSoyad.getText().isEmpty() || txtKayitYas.getText().isEmpty() || jComboBox1.getSelectedItem().toString().equals("Yıl")|| jComboBox2.getSelectedItem().toString().equals("Gün") || jComboBox3.getSelectedItem().toString().equals("Ay") || txtKayitTCKN.getText().isEmpty() || txtKayitTelefon.getText().isEmpty() || txtKayitMail.getText().isEmpty() || txtKayitKullanici.getText().isEmpty() || String.valueOf(txtKayitParola.getPassword()).isEmpty()){
            JOptionPane.showMessageDialog(null,
                "Lütfen tüm alanları eksiksiz bir şekilde doldurunuz. Kullanıcı No program tarafından tanımlanır.",
                "Kayıt Olunamadı",
                JOptionPane.ERROR_MESSAGE);
        }
        else{
            String dogumTarihi = jComboBox2.getSelectedItem().toString() +"/" +jComboBox3.getSelectedItem().toString() +"/"+jComboBox1.getSelectedItem().toString();
            String cinsiyet = "";
            if(jRadioButton2.isSelected()) cinsiyet = "Erkek";
            else cinsiyet ="Kadın";
            String Ktype = "";
            if (isUserKayitOk) Ktype= "Kullanıcı";
            if(isPersonelKayitOk) Ktype= "Personel";
            if(isYoneticiKayitOk) Ktype= "Yönetici";
            i1 = new Kullanici(txtKayitKullanici.getText(),txtKayitAd.getText(),txtKayitSoyad.getText(),Integer.valueOf(txtKayitYas.getText()),dogumTarihi,txtKayitTCKN.getText(),txtKayitTelefon.getText(),cinsiyet,txtKayitMail.getText(),String.valueOf(txtKayitParola.getPassword()),Ktype);
            if(i1.KayitOl()){
                if (JOptionPane.showConfirmDialog(null, "Sistem tarafından sizlere oluşturulan Giriş ID'niz: "+txtKayitKullanici.getText() + "\nGiriş ID' si kopyalanarak devam edilsin mi?", "ID Kopyalansın mı?",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                String myString = txtKayitKullanici.getText();
                StringSelection stringSelection = new StringSelection(myString);
                Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
                clipboard.setContents(stringSelection, null);
                JOptionPane.showMessageDialog(null, "Giriş Id'niz kopyalandı.");
                this.setTitle("Giriş Yap");
                // yes option
            } else {
                // no option
                this.setTitle("Giriş Yap");
            }

            txtKayitKullanici.setText("");
            txtKayitAd.setText("");
            txtKayitSoyad.setText("");
            txtKayitYas.setText("");
            txtKayitTCKN.setText("");
            txtKayitMail.setText("");
            txtKayitTelefon.setText("");
            txtKayitParola.setText("");
            jComboBox1.setSelectedIndex(0);
            jComboBox1.setSelectedIndex(0);
            jComboBox1.setSelectedIndex(0);
            pnlKayitOl.setVisible(false);
            pnlGirisYap.setVisible(true);
            pnlKayitOl.setVisible(false);
        }
            else{
                JOptionPane.showMessageDialog(null,
                "Kayıt olmada beklenmedik bir hata oluştu. Bu bilgilerle eşleşen bilgiler mevcut olabilir. Lütfen fakrlı giriş bilgileri deneyin.",
                "Kayıt Olunamadı",
                JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnKayitOlActionPerformed

