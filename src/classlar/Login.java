    private void btnUserLogin2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUserLogin2ActionPerformed
        // TODO add your handling code here:
        String pType = "";
        if(isUserSifremiUnuttumOk) pType = "Kullanıcı";
        if(isPersonelSifremiUnuttumOk) pType = "Personel";
        if(isYoneticiSifremiUnuttumOk) pType = "Yönetici";
        if(Kullanici.SifremiUnuttum(txtSifremiUnuttum.getText(), pType).equals("")){
            JOptionPane.showMessageDialog(null,
    "Lütfen mail adresini ve kullanıcı tipini doğru seçtiğinizden emin olun.",
    "Kullanıcı Bulunamadı",
    JOptionPane.ERROR_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(null," Şifreniz : "+Kullanici.SifremiUnuttum(txtSifremiUnuttum.getText(), pType));
            txtSifremiUnuttum.setText("");
            pnlGirisYap.setVisible(true);
            pnlSifremiUnuttum.setVisible(false);
            this.setTitle("Giriş Yap");
        }
    }//GEN-LAST:event_btnUserLogin2ActionPerformed

